    <div class="my-2 p-5 rounded bg-white" id="priorodad">
        <h3 class="text-lg">Prioridad</h3>
        <dl class="p-3">
            <dt class="font-bold">Inmediato:</dt>
            <dd>Proyecto o Etapa que debe de finalizarse en la menor cantidad de tiempo posible ya que de no hacerlo, impide el correcto desarrollo de las operaciones, el avance del proyecto o es requerido para el cumplimiento de un requisito externo</dd>

            <dt class="font-bold">Alto:</dt>
            <dd>Proyecto o etapa que debe finalizare en la menor cantidad de tiempo posible, pero puede ser aplazada para dar cumplimiento a un proyecto de priorodad inmediata</dd>

            <dt class="font-bold">Medio:</dt>
            <dd>Proyecto o Etapa que puede cumplirse en un espacio de tiempo mas amplio. ya que no impide el correcto funcionamiento del mismo, no es necesario para cumplir el ningún requisito de ley, puede ser pos-puesto para cumplir con proyectos o etapas de mayor prioridad</dd>

            <dt class="font-bold">Bajo:</dt>
            <dd>Proyecto o Etapa que complementa el proyecto, no impide el desarrollo del mismo, el correcto funcionamiento de los procesos o la funcionalidad de estos, por lo que puede ser pos-puesto para dar cumplimiento a otros proyectos o etapas</dd>
        </dl>
    </div>

